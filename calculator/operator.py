def adition(num1: str, num2: str) -> str:
    return str(float(num1) + float(num2))

def minus(num1: str, num2: str) -> str:
    return str(float(num1) - float(num2))

def multiply(num1: str, num2: str) -> str:
    return str(float(num1) * float(num2))

def divide(num1: str, num2: str) -> str:
    return str(float(num1) / float(num2))

